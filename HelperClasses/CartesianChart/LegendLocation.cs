namespace ModernThemables.HelperClasses.CartesianChart
{
	/// <summary>
	/// Enum describing the location of a legend on a chart.
	/// </summary>
	public enum LegendLocation
	{
		/// <summary>
		/// Legend at the top of the chart.
		/// </summary>
		Top,

		/// <summary>
		/// Legend at the bottom of the chart.
		/// </summary>
		Bottom,

		/// <summary>
		/// Legend on the right hand side of the chart.
		/// </summary>
		Right,

		/// <summary>
		/// Legend on the left hand side of the chart.
		/// </summary>
		Left,

		/// <summary>
		/// No legend on the chart.
		/// </summary>
		None,
	}
}
